#!/bin/python3
# Python Maths

print (50 + 50) # Addition
print (50 - 50) # Subtraction
print (50 / 50) # Division
print (50 * 2)  # Multiply
print (50 ** 2) # Exponents 
print (50 % 6)  # Modulo
print (50 / 6)  # Float Number
print (50 // 6) # Only whats left over.
print (50 + 50 - 50 * 50 / 50) # PEMDAS

# PEMDAS is P, E, MD, AS; multiplication and division have the same precedence, 
# and the same goes for addition and subtraction. 
# When a division operator appears before multiplication, division goes first.
